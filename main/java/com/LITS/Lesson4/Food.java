package com.LITS.Lesson4;

public class Food {
    int price;
    int quantity;
    int weight;

    public Food() {
        int price;
        int quantity;
        int weight;
    }

    public Food(int price, int quantity) {
        this.price = price;
        this.quantity = quantity;
    }

    public Food(int price, int quantity, int weight) {
        this.price = price;
        this.quantity = quantity;
        this.weight = weight;
    }
}
